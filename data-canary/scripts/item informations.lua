function DROP_INFORMATIONS:getInformations(value)
   
    if not value then
       return false
    end
	
	if tonumber(value) then
	   value = getItemName(value)
	end
	
	if not value then
	   return false
	end
	
	value = value:lower()
	local t = self.config[value]
	if not t then
	   return false
	end
	
	
	local str = "You can get this item by hunting these monsters: "
	if t.text then
	   str = t.text
	end
	str = str .. " "
	if t.monsterList then
	    for i, v in pairs(t.monsterList) do
	        str = str .. "{" .. v .. "}"
			if i == #t.monsterList then
			   str = str .. "."
			else
			   str = str .. ", "
			end
	    end
	end
	
	return str
end
	