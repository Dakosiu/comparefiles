end

-- Function called by the callback "npcHandler:setCallback(CALLBACK_GREET, greetCallback)" in end of file
local function greetCallback(npc, player)
	npcHandler:setMessage(MESSAGE_GREET, "Hello |PLAYERNAME|, you need more info about {canary}?")
	return true
end


-- On creature say callback
local function creatureSayCallback(npc, player, type, msg)
	local playerId = player:getId()
	if not npcHandler:checkInteraction(npc, player) then
		return false
	end
	
	local pos = player:getPosition()
	local str = "This buff give: "
	
	if MsgContains(msg, "travel") or MsgContains(msg, "teleport") then
	   npcHandler:say("Are you sure?", npc, player)
	   npcHandler:setTopic(playerId, 1)
	elseif npcHandler:getTopic(playerId) == 1 and MsgContains(msg, "yes") then
	   if not FortressWar:isWinner(player) then
	      return npcHandler:say("Sorry, Only winners Team Fortress event can travel there..", npc, player)
	   end
	   
	   local pos = FortressWar:getIslandPosition()
	   if pos then
	      player:teleportTo(pos)
	   end
	end
	
	
    return true
end

